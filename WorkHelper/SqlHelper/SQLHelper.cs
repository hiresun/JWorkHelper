        public static int ExecuteSql(SqlTransaction trans, List<String> SQLSet, List<SqlParameter[]> cmdParmSet)
        {
            SqlCommand cmd = new SqlCommand();
            //cmd.CommandTimeout = 300;
            int rows = 0;
            //—≠ª∑
            for (int i = 0; i < SQLSet.Count; i++)
